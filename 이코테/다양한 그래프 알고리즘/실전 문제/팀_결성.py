import sys
input = sys.stdin.readline

def find(x):
    if parent[x] != x:
        parent[x] = find(parent[x])
    return parent[x]

def union(a, b):
    a = find(a)
    b = find(b)

    if a < b:
        parent[b] = a
    else:
        parent[a] = b

n, m = map(int, input().split())
parent = [i for i in range(n + 1)]

for _ in range(m):
    o, a, b = map(int, input().split())
    if o:
        a = find(a)
        b = find(b)
        print("YES") if a == b else print("NO")
    else:
        union(a, b)

